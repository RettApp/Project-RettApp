			<div data-role="collapsible" name="mediSortItem1">
				<h2>Wirkstoffklasse</h2>
				<ul>
					<li>nichtsteroidales Antirheumatikum</li>
					<li>Antipyretikum</li>
					<li>Thrombozytenaggregationshemmer</li>
				</ul>
			</div>
			<div data-role="collapsible" name="mediSortItem2">
				<h2>Wirkung</h2>
				<p>Acetylsalicylsäure wirkt durch Hemmung der Cyclooxygenase COX-1 schon in kleinen Dosen (30–50 mg) gerinnungshemmend, mit steigender Dosis (0,5–2 g) durch Hemmung der Cyclooxygenase COX-1 und COX-2 und der daraus folgenden sinkenden Bildung von Prostaglandinen auch dezentral schmerzstillend, antirheumatisch sowie fiebersenkend und schließlich (2–5 g) entzündungshemmend.</p>
			</div>
			<div data-role="collapsible" name="mediSortItem3">
				<h2>Nebenwirkung</h2>
				<p>Höhere Dosen (ab 6-8 g/Tag) von ASS haben eine Ototoxizität und können das Gehör schädigen und Tinnitus verursachen.</p>
				<ul>
					<li>gastrointestinal
						<ul>
							<li>schlechte Magenverträglichkeit, Ulkusneigung</li>
							<li>Blutungen (CAVE: Eisenmangelanämie bei Dauertherapie)</li>
							<li>Ion trapping</li>
						</ul>
					</li>
					<li>hämatologisch
						<ul>
							<li>Thrombozytenaggregationshemmung über mehrere Tage</li>
							<li>verminderte Prothrombinbildung</li>
						</ul>
					</li>
					<li>renal
						<ul>
							<li>Senkung der Nierenperfusion</li>
							<li>bei niedriger Dosis (&lt;2g/d) wird Harnsäuresekretion gehemmt und Urikosurika verlieren ihre Wirkung</li>
							<li>bei hoher Dosis wird die Harnsäurerückresorption gehemmt</li>
						</ul>
					</li>
					<li>neurologisch
						<ul>
							<li>Ohrensausen</li>
							<li>Schwindel</li>
						</ul>
					</li>
					<li>sonstige
						<ul>
							<li>"Aspirinasthma"</li>
							<li>Konstriktion des Ductus arteriosus</li>
							<li>Kontraktionshemmung des Uterus</li>
							<li>Reye-Syndrom</li>
						</ul>
					</li>
				</ul>
				<p>Acetylsalicylsäure zerfällt bei zu langer Lagerung zu Salicylsäure und führt dann zu verstärkten Nebenwirkungen.</p>
