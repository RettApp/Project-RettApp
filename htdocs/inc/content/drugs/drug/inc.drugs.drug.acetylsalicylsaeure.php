					<dd>Wirkungsdauer 3-5 Tage nach Einnahme&frasl;Gabe</dd>
				</dl>
				<dl>
					<dt>Schmerzen</dt>
					<dd>Wirkungsdauer 3-4 Stunden nach Einnahme&frasl;Gabe</dd>
				</dl>
			</div>
