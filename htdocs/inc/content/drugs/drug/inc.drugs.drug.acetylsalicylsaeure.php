	</div>
	<div data-role="content" role="main" class="ui-content">
		<p>Folgende Informationen stehen Ihnen zur Verfügung:</p>
		<div class="listToSort" data-role="collapsibleset" data-collapsed-icon="carat-r" data-expanded-icon="carat-d" data-inset="false">
			<div data-role="collapsible" name="mediSortItem1">
				<h2>Wirkstoffklasse</h2>
				<ul>
					<li>nichtsteroidales Antirheumatikum</li>
					<li>Antipyretikum</li>
					<li>Thrombozytenaggregationshemmer</li>
				</ul>
			</div>
			<div data-role="collapsible" name="mediSortItem2">
				<h2>Wirkung</h2>
				<p>Acetylsalicylsäure wirkt durch Hemmung der Cyclooxygenase COX-1 schon in kleinen Dosen (30–50 mg) gerinnungshemmend, mit steigender Dosis (0,5–2 g) durch Hemmung der Cyclooxygenase COX-1 und COX-2 und der daraus folgenden sinkenden Bildung von Prostaglandinen auch dezentral schmerzstillend, antirheumatisch sowie fiebersenkend und schließlich (2–5 g) entzündungshemmend.</p>
			</div>
			<!-- !Nebenwirkung-->
			<div data-role="collapsible" name="mediSortItem3">
				<h2>Nebenwirkung</h2>
				<p>Höhere Dosen (ab 6-8 g/Tag) von ASS haben eine Ototoxizität und können das Gehör schädigen und Tinnitus verursachen.</p>
				<ul>
					<li>gastrointestinal
						<ul>
							<li>schlechte Magenverträglichkeit, Ulkusneigung</li>
							<li>Blutungen (CAVE: Eisenmangelanämie bei Dauertherapie)</li>
							<li>Ion trapping</li>
						</ul>
					</li>
					<li>hämatologisch
						<ul>
							<li>Thrombozytenaggregationshemmung über mehrere Tage</li>
							<li>verminderte Prothrombinbildung</li>
						</ul>
					</li>
					<li>renal
						<ul>
							<li>Senkung der Nierenperfusion</li>
							<li>bei niedriger Dosis (&lt;2g/d) wird Harnsäuresekretion gehemmt und Urikosurika verlieren ihre Wirkung</li>
							<li>bei hoher Dosis wird die Harnsäurerückresorption gehemmt</li>
						</ul>
					</li>
					<li>neurologisch
						<ul>
							<li>Ohrensausen</li>
							<li>Schwindel</li>
						</ul>
					</li>
					<li>sonstige
						<ul>
							<li>"Aspirinasthma"</li>
							<li>Konstriktion des Ductus arteriosus</li>
							<li>Kontraktionshemmung des Uterus</li>
							<li>Reye-Syndrom</li>
						</ul>
					</li>
				</ul>
				<p>Acetylsalicylsäure zerfällt bei zu langer Lagerung zu Salicylsäure und führt dann zu verstärkten Nebenwirkungen.</p>
			</div>
			<!-- !Interaktion/Wechselwirkungen -->
			<div data-role="collapsible" name="mediSortItem4">
				<h2>Interaktion &frasl; Wechselwirkungen</h2>
				<ul>
					<li>
						<dl>
							<dt>Antikoagulanzien (blutgerinnungshemmende Mittel)</dt>
							<dd>Erhöhung der Blutungsgefahr (gastrointerstinal). Besonders gefährlich sind Kombinationen mit Clopidogrel und Vitamin-K-Antagonisten (Warfarin, Dicoumarol).</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>Antidiabetika</dt>
							<dd>Verstärkt die Wirkung der Antidiabetika</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>Schleifendiuretika, Spironolacton, Canrenoat, Blutdrucksenker und Gichtmittel</dt>
							<dd>Vermindert die Wirkung dieser Medikamente</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>Digoxin, Lithium oder Barbiturate</dt>
							<dd>Konzentration im Blut wird erhöht. Erhöht Vergiftungsrisiko.</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>Methotrexat (Zellgift zur Behandlung von Rheuma und Krebs)</dt>
							<dd>Das Schilddrüsenhormon Trijodthyronin oder Sulfonamid werden in Wirkung und Nebenwirkungen verstärkt</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>Valproinsäure (Antiepileptikum)</dt>
							<dd>Kann zur Verstärkung der Wirkung und Nebenwirkung führen.</dd>
						</dl>
					</li>
					<li>
						<dl>
							<dt>alle nicht-steroidalen Antirheumatika</dt>
							<dd>Konzentration im Blut wird erhöht, dies kann zur Steigerung der Wirkung und Nebenwirkung führen.</dd>
						</dl>
					</li>
				</ul>	
			</div>
			<div data-role="collapsible" name="mediSortItem5">
				<h2>Wirkungseintritt</h2>
				<dl>
					<dt>Thrombozytenaggregationshemmer</dt>
					<dd>Wirkungseintritt bereits nach 2 Minuten</dd>
				</dl>
				<dl>
					<dt>Schmerzen</dt>
					<dd>Wirkungseintritt nach 4-10 Minuten - Wirkungsmaximum nach 20 Minuten</dd>
				</dl>
			</div>
			<div data-role="collapsible" name="mediSortItem6">
